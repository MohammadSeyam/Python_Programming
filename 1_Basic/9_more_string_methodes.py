s = "i am seyam"

# 1: Capitalize
print(s.capitalize())   # output -> I am seyam

# 2: replace(old,new)
print(s.replace("seyam","python"))

# 3: find()
print(s.find("am"))     # if found return first index else return -1

# 4: count()
print(s.count("am"))    # count the occurrence of substring

# 5: upper
print(s.upper())

# 6: lower
print(s.lower())

a = 5
b = 2 

# 1 (sum '+')
print(f"a+b = {a+b}")

# 2 (sub '-')
print(f"a-b = {a-b}")

# 3 (multi '*')
print(f"a*b = {a*b}")

# 4 (div '/')
print(f"a/b = {a/b}")  # return type float

# 5 (pow 'a^b')
print(f"a^b = {a**b}") # T.C = O(logb)

# 6 (mod 'a%b')
print(f"a%b = {a%b}")  # reminder of a/b

# 7 (floor div '//')
print(f"a//b = {a//b}") # return type integer
